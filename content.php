<h1>London</h1>
<p>London is the capital city of England. It is the most populous city in the  United Kingdom, with a metropolitan area of over 13 million inhabitants.</p>
<p>Standing on the River Thames, London has been a major settlement for two millennia, its history going back to its founding by the Romans, who named it Londinium.</p>

<h1>Paris</h1>
<p>Paris is one of the most popular cities in the world that tourists visit every year</p>
<p>It is located next to the Seine River and is known world-wide as the “City of Lights”</p>

<h1>Tokyo</h1>
<p>Tokyo is one of the largest, most modern and crowded cities in the world</p>
<p>With more than 12 million people living in Tokyo, never stopping, you can see the train going out all night on Shinjuku Station, or the stream of people walking on Tokyo's boulevards and streets</p>